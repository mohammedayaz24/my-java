package com.ayaz;

public class Loops {
    public static void main(String[] args){

        //while loop
        System.out.println("while loop");
        int count =1;
        while(count<=5){
            System.out.println(count);
            count++;
        }
        System.out.println("-------------------------------");



        System.out.println("--------------------------------");
        System.out.println("for loop");
        // for loop
        for(int counts = 1; counts <=5; counts++){
            System.out.println(counts);
        }


    }

}
